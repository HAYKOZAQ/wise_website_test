def run_scraper():
    data = []
    
    # Attempt to crawl actual pages from MLSA
    print("Attempting to crawl MLSA social programs...")
    try:
        urls = [
            "https://www.mlsa.am/?page_id=24479", # Example page ID
            "https://www.mlsa.am/?cat=1"
        ]
        headers = {
            "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36"
        }
        for url in urls:
            r = requests.get(url, headers=headers, timeout=10)
            if r.status_code == 200:
                soup = BeautifulSoup(r.text, 'html.parser')
                # Find post/content divs
                content_div = soup.find('div', class_='entry-content') or soup.find('article')
                if content_div:
                    title = soup.find('h1').text.strip() if soup.find('h1') else "MLSA Program Detail"
                    text = content_div.get_text(separator='\n').strip()
                    if text:
                        data.append({"title": title, "content": text})
                        print(f"  Successfully crawled: {title}")
    except Exception as e:
        print(f"Scraper request failed: {e}. Falling back to preloaded database.")

    # Merge or fallback to predefined high-fidelity MLSA data
    crawled_titles = {item["title"] for item in data}
    for fb in FALLBACK_PROGRAMS:
        if fb["title"] not in crawled_titles:
            data.append(fb)

    # Save to local JSON database inside backend/
    os.makedirs("backend/data", exist_ok=True)
    out_file = "backend/data/mlsa_programs.json"
    with open(out_file, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    print(f"Completed! Saved {len(data)} documents to {out_file}")

if __name__ == "__main__":
    run_scraper()
